package oops1;

public class C10 {
	
	private int x= 50;
	int y=60;
	public int z=40;
	protected int p=20;

	public static void main(String[] args) {
		C10 obj= new C10();
		System.out.println(obj.x);
		System.out.println(obj.y);
		System.out.println(obj.z);
		System.out.println(obj.p);
	}

}
