package Array_collections;

public class Array_Add {

	public static void main(String[] args) {
		
		int[][] A= {{4,3,2},{9,8,7}};
		int[][] B= {{9,8,7},{4,7,6}};
		
		for(int i=0;i<=1;i++)
		for(int j=0;j<=2;j++)
		{
			System.out.print(A[i][j]+B[i][j] +"  ");
			
	}
	
		}
	
}

